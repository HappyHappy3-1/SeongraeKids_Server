create or replace function public.validate_school_email(p_email text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_email text := lower(trim(coalesce(p_email, '')));
  v_local_part text;
  v_domain_part text;
  v_domain_exists boolean;
begin
  if v_email = '' then
    raise exception 'school email is required';
  end if;

  if position('@' in v_email) = 0 then
    raise exception 'invalid email format';
  end if;

  v_local_part := split_part(v_email, '@', 1);
  v_domain_part := split_part(v_email, '@', 2);

  select exists (
    select 1
    from public.allowed_email_domains d
    where d.domain = v_domain_part
      and d.is_active = true
  )
  into v_domain_exists;

  if not v_domain_exists then
    raise exception 'Only @e-mirim.hs.kr emails are allowed';
  end if;

  if v_local_part !~ '^s[0-9]{2}-(?:[1-9]|[1-6][0-9]|7[0-2])$' then
    raise exception 'Email local part must match sYY-1 to sYY-72 format';
  end if;
end;
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_email text;
  v_name text;
begin
  v_email := lower(trim(new.email));

  perform public.validate_school_email(v_email);

  v_name := coalesce(
    nullif(trim(new.raw_user_meta_data->>'name'), ''),
    nullif(trim(new.raw_user_meta_data->>'full_name'), ''),
    split_part(v_email, '@', 1)
  );

  insert into public.profiles (
    id,
    school_email,
    name,
    role
  )
  values (
    new.id,
    v_email,
    v_name,
    'student'::public.user_role
  );

  return new;
end;
$$;

